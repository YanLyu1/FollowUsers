package Pojo;

/**
 *
 * @author root
 */
public class User {
    private String uid;
    private String firstname;
    private String lasiname;

    public User() {
    }

    public String getUid() {
        return uid;
    }

    public void setUid(String uid) {
        this.uid = uid;
    }

    public String getFirstname() {
        return firstname;
    }

    public void setFirstname(String firstname) {
        this.firstname = firstname;
    }

    public String getLasiname() {
        return lasiname;
    }

    public void setLasiname(String lasiname) {
        this.lasiname = lasiname;
    }
    
    
}
